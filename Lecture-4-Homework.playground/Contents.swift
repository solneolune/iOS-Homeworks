func sortedMerge(_ arr1: [Int], _ arr2: [Int]) -> [Int] {
    var mergedArr = arr1 + arr2
    
    for i in 0..<mergedArr.count {
        for j in 0..<(mergedArr.count - i - 1) {
            if mergedArr[j] > mergedArr[j + 1] {
                let temp = mergedArr[j]
                mergedArr[j] = mergedArr[j + 1]
                mergedArr[j + 1] = temp
            }
        }
    }
    return mergedArr
}

